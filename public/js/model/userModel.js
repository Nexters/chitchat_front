
function User(_id, name, nickname, gender, likeDrama, joinedChatroom, email, birthday) {
    this._id = _id;
    this.name = name;
    this.nickname = nickname;
    this.gender = gender;
    this.likeDrama = likeDrama;
    this.joinedChatroom = joinedChatroom;
    this.email = email;
    this.birthday = birthday;
}
